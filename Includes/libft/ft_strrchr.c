#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	const char	*ind = 0;
	int			i;

	i = 0;
	if (c == 0)
		return ((char *)str + ft_strlen(str));
	while (str[i])
	{
		if (str[i] == (unsigned char)c)
		{
			ind = str + i;
		}
		i++;
	}
	return ((char *)ind);
}
