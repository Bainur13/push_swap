#include "libft.h"

int	ft_isspace_minus(int c)
{
	if ((c >= 9 && c <= 13) || c == 32 || c == '-')
		return (1);
	return (0);
}

int	ft_isspace(int c)
{
	if ((c >= 9 && c <= 13) || c == 32)
		return (1);
	return (0);
}
